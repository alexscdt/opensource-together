import { ProjectRole } from '@/domain/projectRole/projectRole.entity';
import { ProjectRoleResponseDto } from './project-role-response.dto';

export function toProjectRoleResponseDto(
  projectRole: ProjectRole,
): ProjectRoleResponseDto {
  return {
    id: projectRole.getId() || '',
    projectId: projectRole.getProjectId() || '',
    roleTitle: projectRole.getRoleTitle(),
    description: projectRole.getDescription(),
    isFilled: projectRole.getIsFilled(),
    skillSet: projectRole.getSkillSet().map((tech) => ({
      id: tech.getId(),
      name: tech.getName(),
      iconUrl: tech.getIconUrl(),
    })),
  };
}
